package com.proline.models;

import java.util.ArrayList;
import java.util.List;

public class DBConnSystemModel {
	
	private String id;
	private String xref;
	private String description;
	private List<DBDetailsModel> dbDetailModelList = new ArrayList<DBDetailsModel>();
	
	public String getId() {
		return id;
	} 
	public void setId(String id) {
		this.id = id;
	}
	public String getXref() {
		return xref;
	}
	public void setXref(String xref) {
		this.xref = xref;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public List<DBDetailsModel> getDbDetailModelList() {
		return dbDetailModelList;
	}
	public void setDbDetailModelList(List<DBDetailsModel> dbDetailModelList) {
		this.dbDetailModelList = dbDetailModelList;
	}

}
