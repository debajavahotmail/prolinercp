package com.proline.models.dashboards;

import java.util.Map;

/**
 * @author Debadatta Mishra
 *
 */
public class ChartModel {
	
	private Map<String,Number> dataMap;
	private Map<String,String> toolTipMap;
	private boolean colorFlag;
	private String title; 
	private String xAxisTitle;
	private String yAxisTitle;
	
	public Map<String, Number> getDataMap() {
		return dataMap;
	}
	public void setDataMap(Map<String, Number> dataMap) {
		this.dataMap = dataMap;
	}
	public boolean isColorFlag() {
		return colorFlag;
	}
	public void setColorFlag(boolean colorFlag) {
		this.colorFlag = colorFlag;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Map<String, String> getToolTipMap() {
		return toolTipMap;
	}
	public void setToolTipMap(Map<String, String> toolTipMap) {
		this.toolTipMap = toolTipMap;
	}
	public String getxAxisTitle() {
		return xAxisTitle;
	}
	public void setxAxisTitle(String xAxisTitle) {
		this.xAxisTitle = xAxisTitle;
	}
	public String getyAxisTitle() {
		return yAxisTitle;
	}
	public void setyAxisTitle(String yAxisTitle) {
		this.yAxisTitle = yAxisTitle;
	}

}
