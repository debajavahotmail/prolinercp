package com.proline.models;

public class DBDetailsModel {
	
	private String id;
	private String name;
	private String user;
	private String pq;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUser() { 
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPq() {
		return pq;
	}
	public void setPq(String pq) {
		this.pq = pq;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	

}
